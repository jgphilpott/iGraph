function regionsColourSwitch(region) {
  switch(region) {

    case "Europe & Central Asia":
      return red

    case "Latin America & Caribbean":
      return orange

    case "South Asia":
      return yellow

    case "Midle East & North Africa":
      return green

    case "East Asia & Pacific":
      return blue

    case "North America":
      return purple

    case "Sub-Saharan Africa":
      return pink

    default:
      return black

  }
}
