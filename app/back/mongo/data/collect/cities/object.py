class City():

    def __init__(self, city):

        self.country = city["country"]
        self.id = city["id"]
        self.latitude = city["latitude"]
        self.longitude = city["longitude"]
        self.name = city["name"]
        self.pop_avg = city["pop_avg"]
        self.pop_max = city["pop_max"]
        self.pop_min = city["pop_min"]
        self.rank = city["rank"]
        self.wiki = city["wiki"]
        self.zone = city["zone"]
